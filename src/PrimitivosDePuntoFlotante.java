public class PrimitivosDePuntoFlotante {
    public static void main(String[] args) {
        System.out.println("primitivos de punto flotante");


        System.out.println("float en bytes es igual a : " + Float.BYTES);
        System.out.println("float en bites es igual a : " + Float.SIZE);
        System.out.println("float tiene un maximo valor de: " + Float.MAX_VALUE);
        System.out.println("float tiene un minimo  valor de: " + Float.MIN_VALUE);

        System.out.println("double en bytes es igual a : " + Double.BYTES);
        System.out.println("double en bites es igual a : " + Double.SIZE);
        System.out.println("double tiene un maximo valor de: " + Double.MAX_VALUE);
        System.out.println("double tiene un minimo  valor de: " + Double.MIN_VALUE);







    }

}
